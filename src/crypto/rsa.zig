/// Hash algorithms supported for RSA-PSS and RSA-OAEP padding.
/// Only SHA-1 through SHA-512 are supported by wolfSSL for these padding modes.
pub const PaddingHash = enum {
    sha1,
    sha256,
    sha384,
    sha512,

    fn wcHashType(self: PaddingHash) c.wc_HashType {
        return switch (self) {
            .sha1 => c.WC_HASH_TYPE_SHA,
            .sha256 => c.WC_HASH_TYPE_SHA256,
            .sha384 => c.WC_HASH_TYPE_SHA384,
            .sha512 => c.WC_HASH_TYPE_SHA512,
        };
    }

    fn wcMgf1(self: PaddingHash) c_int {
        return switch (self) {
            .sha1 => c.WC_MGF1SHA1,
            .sha256 => c.WC_MGF1SHA256,
            .sha384 => c.WC_MGF1SHA384,
            .sha512 => c.WC_MGF1SHA512,
        };
    }
};

/// RSA key pair for signing, verification, encryption, and decryption.
pub const RsaKeyPair = struct {
    key: *c.RsaKey,

    /// Generate a new RSA key pair of the given bit size (e.g., 2048, 4096).
    pub fn generate(rng: *random.SecureRng, bits: u32) !RsaKeyPair {
        const key = c.wc_NewRsaKey(null, c.INVALID_DEVID, null) orelse return error.OutOfMemory;
        errdefer _ = c.wc_DeleteRsaKey(key, null);

        var ret = c.wc_MakeRsaKey(key, @intCast(bits), c.WC_RSA_EXPONENT, rng.rng);
        if (ret != 0) return errors.mapCryptoError(ret);

        // Associate RNG with key for operations that need blinding (e.g. decrypt)
        ret = c.wc_RsaSetRNG(key, rng.rng);
        if (ret != 0) return errors.mapCryptoError(ret);

        return .{ .key = key };
    }

    /// Import an RSA private key from PKCS#8 DER encoding
    /// (as produced by `openssl genrsa` or `openssl pkcs8 -topk8 -nocrypt -outform DER`).
    /// Strips the PKCS#8 wrapper using wc_GetPkcs8TraditionalOffset, then imports the
    /// embedded PKCS#1 key.
    pub fn fromPkcs8Der(der: []const u8) !RsaKeyPair {
        const key = c.wc_NewRsaKey(null, c.INVALID_DEVID, null) orelse return error.OutOfMemory;
        errdefer _ = c.wc_DeleteRsaKey(key, null);

        // Find the offset and length of the PKCS#1 key embedded in the PKCS#8 structure
        var trad_offset: c.word32 = 0;
        const trad_len = c.wc_GetPkcs8TraditionalOffset(@constCast(der.ptr), &trad_offset, @intCast(der.len));
        if (trad_len < 0) return errors.mapCryptoError(trad_len);

        var idx: c.word32 = 0;
        const ret = c.wc_RsaPrivateKeyDecode(
            der.ptr + @as(usize, trad_offset),
            &idx,
            key,
            @intCast(trad_len),
        );
        if (ret != 0) return errors.mapCryptoError(ret);
        return .{ .key = key };
    }

    /// Import an RSA private key from PKCS#1 DER encoding
    /// (as produced by `openssl rsa -traditional -outform DER`).
    pub fn fromDer(der: []const u8) !RsaKeyPair {
        const key = c.wc_NewRsaKey(null, c.INVALID_DEVID, null) orelse return error.OutOfMemory;
        errdefer _ = c.wc_DeleteRsaKey(key, null);

        var idx: c.word32 = 0;
        const ret = c.wc_RsaPrivateKeyDecode(der.ptr, &idx, key, @intCast(der.len));
        if (ret != 0) return errors.mapCryptoError(ret);
        return .{ .key = key };
    }

    /// Associate an RNG with this key (needed for decrypt/sign blinding).
    pub fn setRng(self: *RsaKeyPair, rng: *random.SecureRng) !void {
        const ret = c.wc_RsaSetRNG(self.key, rng.rng);
        if (ret != 0) return errors.mapCryptoError(ret);
    }

    pub fn deinit(self: *RsaKeyPair) void {
        // wc_DeleteRsaKey calls wc_FreeRsaKey, which calls mp_forcezero on all
        // private fields (d, p, q, dP, dQ, u) — verified in wolfcrypt/src/rsa.c:606-614.
        _ = c.wc_DeleteRsaKey(self.key, null);
    }

    /// Sign a message hash with PKCS#1 v1.5.
    /// Returns a slice of `out` containing the signature.
    pub fn sign(self: *RsaKeyPair, msg_hash: []const u8, out: []u8, rng: *random.SecureRng) ![]u8 {
        const ret = c.wc_RsaSSL_Sign(
            msg_hash.ptr,
            @intCast(msg_hash.len),
            out.ptr,
            @intCast(out.len),
            self.key,
            rng.rng,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Sign a message hash with RSA-PSS.
    /// algo: hash algorithm for PSS (MGF1 uses the same hash).
    /// salt_len: null for hash-length salt (recommended), or an explicit byte count.
    /// Returns a slice of `out` containing the signature.
    pub fn signPss(
        self: *RsaKeyPair,
        msg_hash: []const u8,
        algo: PaddingHash,
        salt_len: ?u32,
        out: []u8,
        rng: *random.SecureRng,
    ) ![]u8 {
        const ret = c.wc_RsaPSS_Sign_ex(
            msg_hash.ptr,
            @intCast(msg_hash.len),
            out.ptr,
            @intCast(out.len),
            algo.wcHashType(),
            algo.wcMgf1(),
            if (salt_len) |n| @as(c_int, @intCast(n)) else c.RSA_PSS_SALT_LEN_DEFAULT,
            self.key,
            rng.rng,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Decrypt ciphertext with RSA private key.
    /// Returns a slice of `out` containing the plaintext.
    pub fn decrypt(self: *RsaKeyPair, ciphertext: []const u8, out: []u8) ![]u8 {
        const ret = c.wc_RsaPrivateDecrypt(
            ciphertext.ptr,
            @intCast(ciphertext.len),
            out.ptr,
            @intCast(out.len),
            self.key,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Decrypt ciphertext with RSA-OAEP.
    /// algo: hash algorithm for OAEP (MGF1 uses the same hash).
    /// Returns a slice of `out` containing the plaintext.
    pub fn decryptOaep(
        self: *RsaKeyPair,
        ciphertext: []const u8,
        algo: PaddingHash,
        out: []u8,
    ) ![]u8 {
        const ret = c.wc_RsaPrivateDecrypt_ex(
            ciphertext.ptr,
            @intCast(ciphertext.len),
            out.ptr,
            @intCast(out.len),
            self.key,
            c.WC_RSA_OAEP_PAD,
            algo.wcHashType(),
            algo.wcMgf1(),
            null,
            0,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Get the RSA key size in bytes.
    pub fn size(self: *RsaKeyPair) usize {
        const s = c.wc_RsaEncryptSize(self.key);
        return if (s < 0) 0 else @intCast(s);
    }

    /// Get a public-key-only view (borrows the key; caller must not outlive the pair).
    pub fn publicKey(self: *RsaKeyPair) RsaPublicKey {
        return .{ .key = self.key, .owned = false };
    }

    /// Export the private key as PKCS#1 DER bytes.
    /// Returns a slice of `out` containing the DER encoding.
    /// `out` must be large enough; 1300 bytes is sufficient for RSA-2048.
    pub fn exportPrivateDer(self: *RsaKeyPair, out: []u8) ![]u8 {
        const ret = c.wc_RsaKeyToDer(self.key, out.ptr, @intCast(out.len));
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }
};

/// RSA public key for verification and encryption.
/// May be owned (created via fromDer) or borrowed (from RsaKeyPair.publicKey).
/// Call deinit() only on owned keys; it is a no-op for borrowed keys.
pub const RsaPublicKey = struct {
    key: *c.RsaKey,
    owned: bool,

    /// Import an RSA public key from SubjectPublicKeyInfo DER encoding
    /// (as produced by `openssl rsa -pubout -outform DER`).
    pub fn fromDer(der: []const u8) !RsaPublicKey {
        const key = c.wc_NewRsaKey(null, c.INVALID_DEVID, null) orelse return error.OutOfMemory;
        errdefer _ = c.wc_DeleteRsaKey(key, null);
        var idx: c.word32 = 0;
        const ret = c.wc_RsaPublicKeyDecode(der.ptr, &idx, key, @intCast(der.len));
        if (ret != 0) return errors.mapCryptoError(ret);
        return .{ .key = key, .owned = true };
    }

    /// Release key memory. Safe to call on borrowed keys (no-op).
    pub fn deinit(self: *RsaPublicKey) void {
        if (self.owned) _ = c.wc_DeleteRsaKey(self.key, null);
    }

    /// Verify a PKCS#1 v1.5 signature.
    /// Returns error.AuthenticationFailed if the signature does not match.
    /// Supports RSA keys up to 4096 bits. Returns `error.BufferTooSmall`
    /// for larger keys (e.g., RSA-8192).
    pub fn verify(self: RsaPublicKey, msg_hash: []const u8, signature: []const u8) !void {
        // Stack buffer for decoded hash. 512 bytes supports RSA up to 4096 bits.
        // A runtime check rejects keys larger than the buffer rather than
        // silently overflowing (e.g. RSA-8192 would need 1024 bytes).
        var out: [512]u8 = undefined;

        const key_size = c.wc_RsaEncryptSize(self.key);
        if (key_size < 0) return errors.mapCryptoError(key_size);
        if (@as(usize, @intCast(key_size)) > out.len) return error.BufferTooSmall;
        const ret = c.wc_RsaSSL_Verify(
            signature.ptr,
            @intCast(signature.len),
            &out,
            @intCast(out.len),
            self.key,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        const verified_hash = out[0..@intCast(ret)];
        if (!std.mem.eql(u8, msg_hash, verified_hash)) return error.AuthenticationFailed;
    }

    /// Verify an RSA-PSS signature.
    /// algo: hash algorithm for PSS (MGF1 uses the same hash).
    /// salt_len: null for hash-length salt (recommended), or an explicit byte count.
    ///
    /// Note: OpenSSL defaults to maximum-length salt (`-sigopt rsa_pss_saltlen:-2`),
    /// not hash-length salt.  Signatures produced by OpenSSL with default settings
    /// will NOT verify with `salt_len = null`.  Use `verifyPssDiscover` for
    /// cross-implementation verification when the salt length is not known in advance.
    ///
    /// Returns error.AuthenticationFailed if the signature does not match.
    pub fn verifyPss(
        self: RsaPublicKey,
        msg_hash: []const u8,
        sig: []const u8,
        algo: PaddingHash,
        salt_len: ?u32,
    ) !void {
        const key_bytes = c.wc_RsaEncryptSize(self.key);
        if (key_bytes < 0) return errors.mapCryptoError(key_bytes);
        if (@as(usize, @intCast(key_bytes)) > 512) return error.BufferTooSmall;

        // Step 1: RSA public key operation -> intermediate encoded message (EM)
        var em_buf: [512]u8 = undefined;
        const em_len = c.wc_RsaPSS_Verify_ex(
            sig.ptr,
            @intCast(sig.len),
            &em_buf,
            @intCast(em_buf.len),
            algo.wcHashType(),
            algo.wcMgf1(),
            if (salt_len) |n| @as(c_int, @intCast(n)) else c.RSA_PSS_SALT_LEN_DEFAULT,
            self.key,
        );
        if (em_len < 0) {
            // PSS structural failures from RsaUnPad_PSS are signature verification
            // failures, not operational errors:
            //   PSS_SALTLEN_E: salt length in the signature doesn't match expectation.
            //   BAD_PADDING_E: PSS terminator byte (0xbc) is wrong.
            // These are excluded from isBadSignatureError() because that helper is
            // shared with Ed25519/Ed448, where the same codes carry different meaning.
            if (em_len == c.PSS_SALTLEN_E or em_len == c.BAD_PADDING_E)
                return error.AuthenticationFailed;
            if (errors.isBadSignatureError(em_len)) return error.AuthenticationFailed;
            return errors.mapCryptoError(em_len);
        }

        // Step 2: verify PSS padding structure against msg_hash
        const key_bits: c_int = @intCast(@as(usize, @intCast(key_bytes)) * 8);
        const pad_ret = c.wc_RsaPSS_CheckPadding_ex(
            msg_hash.ptr,
            @intCast(msg_hash.len),
            &em_buf,
            @intCast(em_len),
            algo.wcHashType(),
            if (salt_len) |n| @as(c_int, @intCast(n)) else c.RSA_PSS_SALT_LEN_DEFAULT,
            key_bits,
        );
        // Any non-zero from CheckPadding means PSS structure doesn't match
        // the hash (verification failure, not a programming error).
        if (pad_ret != 0) return error.AuthenticationFailed;
    }

    /// Verify an RSA-PSS signature, auto-detecting the salt length from the
    /// PSS encoded message (RFC 8017 §9.1.2 Step 3).
    ///
    /// Use this when the signer used an implementation-defined salt length —
    /// for example, OpenSSL's default maximum-salt PSS (`-sigopt rsa_pss_saltlen:-2`),
    /// where the salt fills all available space in the encoded message and its
    /// length depends on the key size and hash algorithm.
    ///
    /// Requires wolfSSL built with WOLFSSL_PSS_SALT_LEN_DISCOVER.  Without that
    /// build option, wolfSSL does not support salt-length discovery and this
    /// function returns error.AuthenticationFailed regardless of signature validity.
    /// Check `@hasDecl(wolfssl.ffi.c, "RSA_PSS_SALT_LEN_DISCOVER")` at comptime
    /// to detect availability.
    ///
    /// Returns error.AuthenticationFailed if the signature does not match.
    pub fn verifyPssDiscover(
        self: RsaPublicKey,
        msg_hash: []const u8,
        sig: []const u8,
        algo: PaddingHash,
    ) !void {
        const key_bytes = c.wc_RsaEncryptSize(self.key);
        if (key_bytes < 0) return errors.mapCryptoError(key_bytes);
        if (@as(usize, @intCast(key_bytes)) > 512) return error.BufferTooSmall;

        var em_buf: [512]u8 = undefined;
        // RSA_PSS_SALT_LEN_DISCOVER = -2: tells wolfSSL to auto-detect the salt
        // length from the PSS DB structure in RsaUnPad_PSS.  We use the literal
        // value so this function compiles even when WOLFSSL_PSS_SALT_LEN_DISCOVER
        // is absent (the function will then return AuthenticationFailed at runtime).
        const discover: c_int = -2;
        const em_len = c.wc_RsaPSS_Verify_ex(
            sig.ptr,
            @intCast(sig.len),
            &em_buf,
            @intCast(em_buf.len),
            algo.wcHashType(),
            algo.wcMgf1(),
            discover,
            self.key,
        );
        if (em_len < 0) {
            if (em_len == c.PSS_SALTLEN_E or em_len == c.BAD_PADDING_E)
                return error.AuthenticationFailed;
            if (errors.isBadSignatureError(em_len)) return error.AuthenticationFailed;
            return errors.mapCryptoError(em_len);
        }

        const key_bits: c_int = @intCast(@as(usize, @intCast(key_bytes)) * 8);
        const pad_ret = c.wc_RsaPSS_CheckPadding_ex(
            msg_hash.ptr,
            @intCast(msg_hash.len),
            &em_buf,
            @intCast(em_len),
            algo.wcHashType(),
            discover,
            key_bits,
        );
        if (pad_ret != 0) return error.AuthenticationFailed;
    }

    /// Encrypt plaintext with RSA public key.
    /// Returns a slice of `out` containing the ciphertext.
    pub fn encrypt(self: RsaPublicKey, plaintext: []const u8, out: []u8, rng: *random.SecureRng) ![]u8 {
        const ret = c.wc_RsaPublicEncrypt(
            plaintext.ptr,
            @intCast(plaintext.len),
            out.ptr,
            @intCast(out.len),
            self.key,
            rng.rng,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Encrypt plaintext with RSA-OAEP.
    /// algo: hash algorithm for OAEP (MGF1 uses the same hash).
    /// Returns a slice of `out` containing the ciphertext.
    pub fn encryptOaep(
        self: RsaPublicKey,
        plaintext: []const u8,
        algo: PaddingHash,
        out: []u8,
        rng: *random.SecureRng,
    ) ![]u8 {
        const ret = c.wc_RsaPublicEncrypt_ex(
            plaintext.ptr,
            @intCast(plaintext.len),
            out.ptr,
            @intCast(out.len),
            self.key,
            rng.rng,
            c.WC_RSA_OAEP_PAD,
            algo.wcHashType(),
            algo.wcMgf1(),
            null,
            0,
        );
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }

    /// Export the public key as SubjectPublicKeyInfo DER bytes.
    /// Returns a slice of `out` containing the DER encoding.
    /// `out` must be large enough; 400 bytes is sufficient for RSA-2048.
    pub fn exportDer(self: RsaPublicKey, out: []u8) ![]u8 {
        const ret = c.wc_RsaKeyToPublicDer(self.key, out.ptr, @intCast(out.len));
        if (ret < 0) return errors.mapCryptoError(ret);
        return out[0..@intCast(ret)];
    }
};

test "RSA generate, sign, verify" {
    var rng = try random.SecureRng.init();
    defer rng.deinit();

    var kp = try RsaKeyPair.generate(&rng, 2048);
    defer kp.deinit();

    // Hash a message
    const msg_hash = [_]u8{0xab} ** 32; // pretend SHA-256 hash

    var sig_buf: [256]u8 = undefined;
    const sig = try kp.sign(&msg_hash, &sig_buf, &rng);

    const pub_key = kp.publicKey();
    try pub_key.verify(&msg_hash, sig);

    // Tamper with hash -> should not verify
    var bad_hash = msg_hash;
    bad_hash[0] ^= 0xff;
    try std.testing.expectError(error.AuthenticationFailed, pub_key.verify(&bad_hash, sig));
}

test "RSA encrypt/decrypt" {
    var rng = try random.SecureRng.init();
    defer rng.deinit();

    var kp = try RsaKeyPair.generate(&rng, 2048);
    defer kp.deinit();

    const plaintext = "hello RSA";
    const pub_key = kp.publicKey();
    var ct_buf: [256]u8 = undefined;
    const ct = try pub_key.encrypt(plaintext, &ct_buf, &rng);

    var pt_buf: [256]u8 = undefined;
    const pt = try kp.decrypt(ct, &pt_buf);
    try std.testing.expectEqualSlices(u8, plaintext, pt);
}

