// PKCS#8 import KAT: import kat_pkcs8_der (same key as kat_priv_der, PKCS#8 wrapped),
// sign kat_sha256_test, and verify the signature matches kat_pkcs1_sig.
// PKCS#1 v1.5 signing is deterministic, so the PKCS#8-imported key must produce
// the identical signature as the PKCS#1-imported key.
test "RSA PKCS#8 import KAT" {
    var rng = try random.SecureRng.init();
    defer rng.deinit();

    var kp = try RsaKeyPair.fromPkcs8Der(&kat_pkcs8_der);
    defer kp.deinit();
    try kp.setRng(&rng);

    var sig_buf: [256]u8 = undefined;
    const sig = try kp.sign(&kat_sha256_test, &sig_buf, &rng);
    try std.testing.expectEqualSlices(u8, &kat_pkcs1_sig, sig);
}

// RSA private key export KAT: imports kat_priv_der via fromDer, exports it back
// via exportPrivateDer, and compares the bytes to kat_priv_der.
// Proves that import→export round-trips the PKCS#1 DER exactly.
// Independent oracle: the OpenSSL-generated kat_priv_der constant.
test "RSA exportPrivateDer KAT" {
    var kp = try RsaKeyPair.fromDer(&kat_priv_der);
    defer kp.deinit();

    var out: [1300]u8 = undefined;
    const der = try kp.exportPrivateDer(&out);
    try std.testing.expectEqualSlices(u8, &kat_priv_der, der);
}

// RSA public key export: imports kat_priv_der, exports the public key DER,
// re-imports it via RsaPublicKey.fromDer, then verifies kat_pkcs1_sig against
// kat_sha256_test. The oracle is the externally-generated kat_pkcs1_sig.
test "RSA exportDer (public key) round-trip" {
    var kp = try RsaKeyPair.fromDer(&kat_priv_der);
    defer kp.deinit();

    var pub_der_buf: [400]u8 = undefined;
    const pub_der = try kp.publicKey().exportDer(&pub_der_buf);

    var pub_key = try RsaPublicKey.fromDer(pub_der);
    defer pub_key.deinit();

    try pub_key.verify(&kat_sha256_test, &kat_pkcs1_sig);
}
