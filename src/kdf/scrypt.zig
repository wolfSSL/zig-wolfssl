const std = @import("std");
const c = @import("../c.zig").c;
const errors = @import("../errors.zig");

/// scrypt (RFC 7914) key derivation function.
pub const Scrypt = struct {
    /// Derive a key using scrypt (RFC 7914).
    ///
    /// `log2_n` is the cost parameter expressed as log2(N), so N=1024 → log2_n=10.
    /// `r` is the block size parameter. `p` is the parallelization parameter.
    /// wolfSSL's `wc_scrypt` takes `cost` = log2(N) directly.
    ///
    /// Empty password or salt slices are passed as NULL to wolfSSL, as required
    /// by RFC 7914 §12 test case 1.
    pub fn deriveKey(password: []const u8, salt: []const u8, log2_n: u6, r: u32, p: u32, out: []u8) !void {
        const passwd_ptr: ?[*]const u8 = if (password.len == 0) null else password.ptr;
        const salt_ptr: ?[*]const u8 = if (salt.len == 0) null else salt.ptr;
        const ret = c.wc_scrypt(
            out.ptr,
            passwd_ptr,
            @intCast(password.len),
            salt_ptr,
            @intCast(salt.len),
            @intCast(log2_n),
            @intCast(r),
            @intCast(p),
            @intCast(out.len),
        );
        if (ret != 0) return errors.mapCryptoError(ret);
    }
};

// RFC 7914 §12.1 — N=16 (log2_n=4), r=1, p=1, password="", salt=""
test "scrypt KAT RFC 7914 TC1" {
    var out: [64]u8 = undefined;
    try Scrypt.deriveKey("", "", 4, 1, 1, &out);
    const expected = [_]u8{
        0x77, 0xd6, 0x57, 0x62, 0x38, 0x65, 0x7b, 0x20,
        0x3b, 0x19, 0xca, 0x42, 0xc1, 0x8a, 0x04, 0x97,
        0xf1, 0x6b, 0x48, 0x44, 0xe3, 0x07, 0x4a, 0xe8,
        0xdf, 0xdf, 0xfa, 0x3f, 0xed, 0xe2, 0x14, 0x42,
        0xfc, 0xd0, 0x06, 0x9d, 0xed, 0x09, 0x48, 0xf8,
        0x32, 0x6a, 0x75, 0x3a, 0x0f, 0xc8, 0x1f, 0x17,
        0xe8, 0xd3, 0xe0, 0xfb, 0x2e, 0x0d, 0x36, 0x28,
        0xcf, 0x35, 0xe2, 0x0c, 0x38, 0xd1, 0x89, 0x06,
    };
    try std.testing.expectEqualSlices(u8, &expected, &out);
}

